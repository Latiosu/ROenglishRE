        if not result == true then
          return false, msg
        end
      end
      if nil ~= DESC.EffectID then
        result, msg = AddItemEffectInfo(ItemID, DESC.EffectID)
      end
      if not result == true then
        return false, msg
      end
      if nil ~= DESC.costume then
        result, msg = AddItemIsCostume(ItemID, DESC.costume)
      end
      if not result == true then
        return false, msg
      end
    end
  end
  return true, "good"
end

